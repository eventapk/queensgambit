class Participant {
  int sNo;
  final String name;
  final String event;
  final String phone;
  final String email;
  final String age;
  final String dob;
  final String gender;
  final String status;
  final String eventDate;

  Participant({
    required this.sNo,
    required this.name,
    required this.event,
    required this.phone,
    required this.email,
    required this.age,
    required this.dob,
    required this.gender,
    required this.status,
    required this.eventDate,
  });
}